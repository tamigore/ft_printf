#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *src)
{
	char	*copy;
	int		i;

	if (!src)
		return (NULL);
	i = ft_strlen(src);
	if (!(copy = (char *)malloc(i + 1)))
		return (0);
	i = 0;
	while (src[i])
	{
		copy[i] = src[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
